def translate():
    word = TextBlob(input.get())
    lan=word.detect_language()
    lan_todict=language.get()
    lan_to=lang_dict[lan_todict]
    word = word.translate(from_lang = lan, to= lan_to)
    output.set(word)



def exit():
    rr = messagebox.askyesnocancel('Exit','Do u want to exit the program',parent=root)
    if(rr==True):
        root.destroy
    
